/**
 * Get communes by wilaya code
 */
function getCommunesByWilaya($wilayaCode) {
    global $COMMUNES;
    return $COMMUNES[$wilayaCode] ?? [];
}

/**
 * Get communes by wilaya name (e.g. "16 - الجزائر (Alger)")
 */
function getCommunesByWilayaName($wilayaName) {
    $code = intval($wilayaName);
    if ($code > 0) {
        return getCommunesByWilaya($code);
    }
    return [];
}
